  _startSTT() {
    if (!('webkitSpeechRecognition' in window) && !('SpeechRecognition' in window)) return;
    if (this._sttRecognition) { try { this._sttRecognition.stop(); } catch(e) {} }
    const SR = window.SpeechRecognition || window.webkitSpeechRecognition;
    const r = new SR();
    r.lang = {ko:'ko-KR',en:'en-US',ja:'ja-JP',zh:'zh-CN'}[this.lang] || 'ko-KR';
    const isMobile = /Android|iPhone|iPad|iPod/i.test(navigator.userAgent);
    r.continuous = !isMobile; // 모바일 전체: 단발 인식 (소리 방지)
    r.interimResults = true;
    r.onresult = e => {
      // 기존 텍스트에 이어붙이기
      const newText = Array.from(e.results).map(r => r[0].transcript).join('');
      const ta = document.getElementById('voice-ta');
      if (e.results[0].isFinal) {
        // 최종 인식: 기존 텍스트 뒤에 추가
        ta.value = (ta.value ? ta.value + ' ' : '') + newText;
      } else {
        // 중간 인식: 임시 표시
        const base = ta.dataset.base || '';
        ta.value = base + (base ? ' ' : '') + newText;
      }
    };
    r.onend = () => {
      // 창이 열려있으면 자동 재시작 (삼성브라우저 제외)
      const ta = document.getElementById('voice-ta');
      if (ta) ta.dataset.base = ta.value; // 현재까지 텍스트 저장
      if (!isMobile && document.getElementById('voice-box').classList.contains('show')) {
        setTimeout(() => { try { r.start(); } catch(e) {} }, 300);
      }
    };
    this._sttRecognition = r;
    try { r.start(); } catch(e) {}
  },

